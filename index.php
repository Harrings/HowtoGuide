<h2>About this Guide</h2>
<p> Debugging in PHP can be difficult as there is little debugging features built in to the language naturally.
<br> since there is no compiling and all the parsing is done on the server it can be difficult to spot errors
<br> and track how data is flowing from page to page what value is stored in variables. </p>

<p> Added into this equation is that arrays in PHP are set up in a Key Value format which adds even more information
<br> for you to keep track of throughout your project. Currently the main functions used in PHP to view the contents of 
<br> variables is <strong>var_dump()</strong> and <strong>debug_backtrace()</strong> but they often do a poor job of displaying the information in an 
<br> easy to read format that allows you to quickly find what you are looking for.</p> 

<p> This is where Kint steps in it allows you to view the contents of variables and arrays in a much easier to digest format.
<br>In this guide we will cover what Kint it is and basic overview of how it works, cover a few of the advantage it has over the existing
<br><strong>var_dump()</strong> and <strong>debug_backtrace()</strong>, how to do basic setup of Kint, what the basic functions in Kint do and how to modify them, and then we will
<br> cover some tips and more advanced uses such as how to customize the tool a bit to fit your needs. </p>


